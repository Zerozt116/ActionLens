from __future__ import annotations

import argparse
import csv
import json
from collections import Counter, defaultdict
from dataclasses import asdict, dataclass
from pathlib import Path


DEFAULT_ACTION_IDS = {
    1,
    10,
    11,
    12,
    14,
    15,
    17,
    27,
    29,
    48,
    57,
    59,
    70,
    74,
    79,
    80,
}


@dataclass(frozen=True)
class AvaSubsetRow:
    video_id: str
    timestamp: int
    x1: float
    y1: float
    x2: float
    y2: float
    action_id: int
    action: str
    action_name: str
    action_type: str
    person_id: int


def main() -> None:
    parser = argparse.ArgumentParser(description="Build a balanced AVA action-evaluation subset manifest.")
    parser.add_argument("manifest", type=Path, help="Full AVA manifest generated by prepare_ava.py.")
    parser.add_argument("--actions", type=int, nargs="+", default=sorted(DEFAULT_ACTION_IDS), help="Action IDs to sample.")
    parser.add_argument("--samples-per-action", type=int, default=20, help="Maximum rows per action ID.")
    parser.add_argument("--max-per-video-action", type=int, default=3, help="Maximum rows from the same video for one action.")
    parser.add_argument("--output", type=Path, default=Path("data/ava/ava_eval_subset.csv"))
    parser.add_argument("--json-output", type=Path, default=Path("data/ava/ava_eval_subset.json"))
    parser.add_argument("--summary-output", type=Path, default=Path("data/ava/ava_eval_subset_summary.json"))
    args = parser.parse_args()

    rows = build_subset(
        manifest_path=args.manifest,
        action_ids=set(args.actions),
        samples_per_action=args.samples_per_action,
        max_per_video_action=args.max_per_video_action,
    )
    args.output.parent.mkdir(parents=True, exist_ok=True)
    write_csv(args.output, rows)
    args.json_output.write_text(json.dumps([asdict(row) for row in rows], ensure_ascii=False, indent=2), encoding="utf-8")
    args.summary_output.write_text(json.dumps(build_summary(rows), ensure_ascii=False, indent=2), encoding="utf-8")

    print(f"wrote {len(rows)} rows to {args.output}")
    print(f"wrote summary to {args.summary_output}")


def build_subset(
    manifest_path: Path,
    action_ids: set[int],
    samples_per_action: int,
    max_per_video_action: int,
) -> list[AvaSubsetRow]:
    if not manifest_path.exists():
        raise FileNotFoundError(f"Manifest does not exist: {manifest_path}")

    by_action: dict[int, list[AvaSubsetRow]] = defaultdict(list)
    with manifest_path.open("r", encoding="utf-8", newline="") as file:
        reader = csv.DictReader(file)
        for record in reader:
            action_id = int(record["action_id"])
            if action_id not in action_ids:
                continue
            by_action[action_id].append(
                AvaSubsetRow(
                    video_id=record["video_id"],
                    timestamp=int(float(record["timestamp"])),
                    x1=float(record["x1"]),
                    y1=float(record["y1"]),
                    x2=float(record["x2"]),
                    y2=float(record["y2"]),
                    action_id=action_id,
                    action=record["action"],
                    action_name=record["action_name"],
                    action_type=record["action_type"],
                    person_id=int(record["person_id"]),
                )
            )

    selected: list[AvaSubsetRow] = []
    for action_id in sorted(action_ids):
        candidates = sorted(
            by_action.get(action_id, []),
            key=lambda row: (row.video_id, row.timestamp, row.person_id),
        )
        per_video_counts: Counter[str] = Counter()
        seen_keys: set[tuple[str, int, int]] = set()
        for row in candidates:
            if len([item for item in selected if item.action_id == action_id]) >= samples_per_action:
                break
            if per_video_counts[row.video_id] >= max_per_video_action:
                continue
            key = (row.video_id, row.timestamp, row.person_id)
            if key in seen_keys:
                continue
            seen_keys.add(key)
            per_video_counts[row.video_id] += 1
            selected.append(row)

    return sorted(selected, key=lambda row: (row.action_type, row.action_id, row.video_id, row.timestamp, row.person_id))


def build_summary(rows: list[AvaSubsetRow]) -> dict[str, object]:
    return {
        "num_rows": len(rows),
        "num_videos": len({row.video_id for row in rows}),
        "by_action_type": dict(Counter(row.action_type for row in rows)),
        "by_action": [
            {
                "action_id": action_id,
                "action": action,
                "action_name": action_name,
                "action_type": action_type,
                "count": count,
            }
            for (action_id, action, action_name, action_type), count in sorted(
                Counter((row.action_id, row.action, row.action_name, row.action_type) for row in rows).items()
            )
        ],
        "video_ids": sorted({row.video_id for row in rows}),
    }


def write_csv(path: Path, rows: list[AvaSubsetRow]) -> None:
    fieldnames = [
        "video_id",
        "timestamp",
        "x1",
        "y1",
        "x2",
        "y2",
        "action_id",
        "action",
        "action_name",
        "action_type",
        "person_id",
    ]
    with path.open("w", encoding="utf-8", newline="") as file:
        writer = csv.DictWriter(file, fieldnames=fieldnames)
        writer.writeheader()
        for row in rows:
            writer.writerow(asdict(row))


if __name__ == "__main__":
    main()
